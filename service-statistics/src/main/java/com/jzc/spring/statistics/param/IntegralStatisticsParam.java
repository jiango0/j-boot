package com.jzc.spring.statistics.param;

public class IntegralStatisticsParam {

    private String shop_id;

    private String shop_name;

    private String address;

    private Integer max_v1;

    private Integer max_v2;

    private Integer max_v3;

    public String getShop_id() {
        return shop_id;
    }

    public void setShop_id(String shop_id) {
        this.shop_id = shop_id;
    }

    public String getShop_name() {
        return shop_name;
    }

    public void setShop_name(String shop_name) {
        this.shop_name = shop_name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public Integer getMax_v1() {
        return max_v1;
    }

    public void setMax_v1(Integer max_v1) {
        this.max_v1 = max_v1;
    }

    public Integer getMax_v2() {
        return max_v2;
    }

    public void setMax_v2(Integer max_v2) {
        this.max_v2 = max_v2;
    }

    public Integer getMax_v3() {
        return max_v3;
    }

    public void setMax_v3(Integer max_v3) {
        this.max_v3 = max_v3;
    }
}
